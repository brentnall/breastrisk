double Density::Calculate_expected_rate(int age, double bmi, int density_measure, int country) 
{
	double result;

	double bmiclipped = ((bmi<15.0) ? 15.0 : bmi);

	int bmiidx = round((bmiclipped-15) *10);
	
	bmiidx = ((bmiidx>=DENSITY_BMIGRP) ? (DENSITY_BMIGRP - 1) : bmiidx);
	
	int ageidx = ((age<40) ? 0 : age - 40);

	result = denMeas[country][density_measure].intercept + denMeas[country][density_measure].age[ageidx] + denMeas[country][density_measure].bmi[bmiidx];

	return result;
}

/* Main density calculations. Based on density residuals that adjust for age and BMI */
double Density::Calculate_rate(int age, double bmi, int density_measure, int country, double density)
{
	double result;
	double expected;
	double observed;
	double density_min;

	int country_choice = country;

        observed = 0; //initialise
            
	if(country_choice >1){
		country_choice = 0; //use UK unless Sweden
	}
	
        if( density_measure ==0 )
        {
            /* VOLPARA */
            density_min = ( (density<2) ? 2: density);
	
      	    density_min = ( (density>30) ? 30: density);
        }
		else if(density_measure==1)
        {
			/* VAS */
            density_min = ( (density<1) ? 1: density);
	
			density_min = ( (density>99) ? 99: density);
		}
        else if(density_measure==2)
        {
            /* BIRADS */
            density_min = ( (density<1) ? 1: density);

            density_min = ( (density>4) ? 4: density);
        }
        else if (density_measure==3)
        {
            /* STRATUS */
            density_min = ( (density<0) ? 0: density);

            density_min = ( (density>100) ? 100: density);
        }

        expected = Calculate_expected_rate(age, bmi, density_measure, country_choice);

	if(density_measure==0)
        {
            observed = log(density_min) - expected; //VOLPARA %

            observed = ( (observed < -1.2) ? -1.2 : observed );  //risk flat beyond this point

            observed = ( (observed > 1.2) ? 1.2 : observed ); 
            
        }
        else if(density_measure==1)
        {
            observed = sqrt(density_min) - expected; // VAS %

    	    result = exp( denMeas[country_choice][density_measure].riskcalib * observed ) / denMeas[country_choice][density_measure].meanrate;

        }
        else if(density_measure==2)
        {
            observed = density_min - expected; // BIRADS

        }
        else if(density_measure==3)
        {
            observed = sqrt(density_min) - expected; // STRATUS %
        }

		
        result = exp( denMeas[country_choice][density_measure].riskcalib * observed ) / denMeas[country_choice][density_measure].meanrate;

	return result;
}
